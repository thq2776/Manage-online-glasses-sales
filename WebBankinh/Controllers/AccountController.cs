        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            string hashPass = HashPassword(model.Password);
            var checkLogin = await db.TaiKhoans.Where(x => x.Username == model.UserName && x.Password == hashPass).FirstOrDefaultAsync();
            if(checkLogin != null)
            {               
                var getInfo = await db.NguoiDungs.Where(x => x.IdTaiKhoan == checkLogin.Id).FirstOrDefaultAsync();
                if (checkLogin != null && checkLogin.LoaiTaiKhoan == "1" || checkLogin.LoaiTaiKhoan == "2")
                {
                    if (getInfo != null)
                    {
                        Session["Admin"] = getInfo;
                    }
                    return RedirectToAction("Index", "Admin/home");
                }
                else
                {
                    if (getInfo != null)
                    {
                        Session["User"] = getInfo;
                    }
                    return RedirectToAction("Index", "Home");
                }
            }
            else
            {
                ModelState.AddModelError("", "Tên đăng nhập hoặc mật khẩu không chính xác!");
                return View("Login");
            }
        }

